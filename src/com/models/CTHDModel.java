public class CTHDModel {
    private int MADU;
    private int MaHD;
    private int SoLuong;
    private double Gia;

    public CTHDModel() {
    }

    public CTHDModel(int MADU, int MaHD, int SoLuong, double Gia) {
        this.MADU = MADU;
        this.MaHD = MaHD;
        this.SoLuong = SoLuong;
        this.Gia = Gia;
    }

    public int getMADU() {
        return MADU;
    }

    public void setMADU(int MADU) {
        this.MADU = MADU;
    }

    public int getMaHD() {
        return MaHD;
    }

    public void setMaHD(int MaHD) {
        this.MaHD = MaHD;
    }

    public int getSoLuong() {
        return SoLuong;
    }

    public void setSoLuong(int SoLuong) {
        this.SoLuong = SoLuong;
    }

    public double getGia() {
        return Gia;
    }

    public void setGia(double Gia) {
        this.Gia = Gia;
    }

    
  
  
    
}
